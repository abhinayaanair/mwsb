function switchLanguage() {
  const selectedLanguage = document.getElementById("language").value;

  // Save the selected language in localStorage
  localStorage.setItem("selectedLanguage", selectedLanguage);
  const keys = [
    "home",
    "about",
    "messages",
    "management",
    "gallery",
    "contact",
  ];
  const dropdownKeys = ["presidentDesk", "secretaryDesk"];
  const navLinks = document.querySelector(".nav-links");
  const mobileNavLinks = document.querySelector("#mobileNav");
  const dropdownLinks = document.querySelector("#messagesDropdown");
  const dropdownLinksMobile = document.querySelector("#messagesDropdownMobile");

  // Update main nav links
  navLinks?.querySelectorAll(".nav-link").forEach((link, index) => {
    if (keys[index]) {
      link.textContent = navTranslations[selectedLanguage][keys[index]];
    }
  });

  // Update mobile nav links
  mobileNavLinks?.querySelectorAll(".nav-link").forEach((link, index) => {
    if (keys[index]) {
      link.textContent = navTranslations[selectedLanguage][keys[index]];
      link.style.fontSize = "10px";
    }
  });

  // Update dropdowns
  dropdownLinks?.querySelectorAll(".dropdown-link").forEach((link, index) => {
    link.textContent = navTranslations[selectedLanguage][dropdownKeys[index]];
  });

  dropdownLinksMobile
    ?.querySelectorAll(".dropdown-link")
    .forEach((link, index) => {
      link.textContent = navTranslations[selectedLanguage][dropdownKeys[index]];
    });
  // Updated keys to match new navigation order
  
  const content = document.querySelector(".text-content");
  const readMoreBtn = document.getElementById("readMoreBtn");
  const readMoreBtnDefault = document.getElementById("readMoreBtnDefault");
  let maxTextLength = selectedLanguage === "ml" ? 200 : 300;
  const isMobile = window.innerWidth <= 768;
  const tasks = document.querySelector(".tasks");
  const certificateDescription = document.querySelectorAll(".certificate-description");

  // Apply Malayalam font styling
  if (selectedLanguage === "ml") {
    content.classList.add("malayalam");
    tasks.classList.add("malayalam");
  } else {
    content.classList.remove("malayalam");
    tasks.classList.remove("malayalam");
  }

 
  // Update tasks
  if (tasks) {
    tasks.querySelectorAll(".tasks-performed").forEach((task, index) => {
      task.querySelector(".tasks-heading").innerHTML =
        translations[selectedLanguage]["tasks"][`task${index + 1}`]["header"];
      task.querySelector(".tasks-content").textContent =
        translations[selectedLanguage]["tasks"][`task${index + 1}`]["content"];
    });
  }

  // Update certificate description
  certificateDescription.forEach((off, index) => {
    off.innerHTML = translations[selectedLanguage]["official"][`block${index + 1}`];
  });

  // Handle hero text truncation
  const fullText = translations[selectedLanguage]["leftHero"];
  if (isMobile && fullText.length > maxTextLength) {
    readMoreBtnDefault.style.display = "none";
    const truncatedText = fullText.slice(0, maxTextLength) + "...";
    content.textContent = truncatedText;
    content.style.textAlign = "center";
    readMoreBtn.style.display = "inline-block";
    readMoreBtn.textContent = "Read More";
  } else {
    content.textContent = fullText;
    readMoreBtn.style.display = "none";
  }
}



// Load stored language preference and initialize
window.addEventListener("load", function () {
  const storedLanguage = localStorage.getItem("selectedLanguage") || "en";
  document.getElementById("language").value = storedLanguage;
  switchLanguage();
  setupDropdownToggle();
});

// Event listeners
document.getElementById("language").addEventListener("change", switchLanguage);

document.getElementById("readMoreBtn").addEventListener("click", function () {
  const selectedLanguage = document.getElementById("language").value;
  const content = document.querySelector(".text-content");
  const readMoreBtn = this;
  const maxTextLength = selectedLanguage === "ml" ? 200 : 300;
  const fullText = translations[selectedLanguage]["leftHero"];

  if (readMoreBtn.textContent === "Read More") {
    content.textContent = fullText;
    readMoreBtn.textContent = "Read Less";
  } else {
    const truncatedText = fullText.slice(0, maxTextLength) + "...";
    content.textContent = truncatedText;
    readMoreBtn.textContent = "Read More";
  }
});

